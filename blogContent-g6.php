        <h2>Comment prévenir la listériose et la toxoplasmose?</h2>

       <p>La bactérie listeria, très répandue dans l’environnement, peut se retrouver dans les aliments d’origine végétale ou animale, même s’ils ont été réfrigéré. Pour limiter tout risque de listériose, évitez les fromages au lait cru, les fromages râpés industriels, les croutes de fromages, et les fromages à pâtes molles. Evitez les poissons crus et fumés ainsi que les coquillages crus.</p>

      <p>La toxoplasmose est due à un parasite présent dans la terre, et donc sur les végétaux ou dans la viande. On peut être contaminé si l’on consomme des aliments mal lavés ou peu cuits. Une prise de sang au début de votre grossesse indique si vous avez déjà eu la toxoplasmose. Si c’est le cas, vous êtes immunisée. Sinon, vous n’êtes pas protégée et des prises de sang vous seront prescrites régulièrement pour vérifier que n’êtes pas infectée.</p>

      <ul>
        <li>Lavez bien les fruits et légumes avant de les consommer</li>
        <li>Mangez de la viande bien cuite</li>
        <li>Lavez-vous les mains avant chaque repas et après avoir manipulé de la viande crue, des crudités avec de la terre.</li>
