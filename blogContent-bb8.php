        <h2>Le sommeil de bébé</h2>

        <p>
          Chaque enfant est unique. Respectez son individualité. Aucun enfant ne correspondra exactement au schéma général du comportement attendu à un certain âge ou à un degré de développement. Il est donc impossible de prétendre avec certitude quand votre enfant devra faire ses nuits.
        </p>
        <p>
          En moyenne, entre trois et six mois, les bébés développent un rythme jour/nuit.
        </p>
        <ul>
         <li>Marquez bien la différence entre le sommeil de jour et le sommeil de nuit. Quand vous vous levez la nuit pour nourrir votre bébé, restez dans la pénombre, n’allumez pas la télévision et n’écoutez pas de musique.<br/>

         Évitez de jouer ou de stimuler votre bébé, avant de le remettre dans son berceau. Durant le jour, laissez-le dormir dans une pièce éclairée, où on entend les bruits habituels de la journée. Le jour, chantez pour votre bébé, jouez avec lui, et d’une manière générale, ne changez pas vos habitudes.</li>

         <li>Veillez à ce que votre bébé fasse souvent la sieste pendant la journée. Un nourrisson trop fatigué ne dormira pas mieux pendant la nuit.</li>
         <li>Suivez la même routine tous les soirs. Cela ne sera peut-être pas possible durant les premiers mois.</li>
         <li>Une fois que votre bébé sera plus âgé, donnez-lui un bain chaud le soir, puis bercez-le et restez au calme. Cette routine signalera à votre bébé que c’est bientôt le moment de dormir.</li>
         <li>Autant que possible, laissez votre bébé s’endormir tout seul de manière à ce qu’il apprenne à s’apaiser lui-même.</li>
        </ul>
