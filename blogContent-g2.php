        <h2>3 échographies essentielles</h2>

       <p>Trois échographies sont recommandées au cours de la grossesse mais d’autres peuvent être proposées par votre médecin.</p>


      <ul>
        <li>Une sonde d’échographie est posée sur le ventre après application d’un gel qui facilite le passage des ultrasons.</li>
        <li>Une échographie par sonde endovaginale (sonde introduite dans le vagin) peut être proposée afin d’améliorer la visualisation de certaines parties du fœtus ou de ses annexes (placenta, membranes, cordon, liquide amniotique).</li>
      </ul>

      <h3 style="color:#6cc; margin-bottom:20px;">1. Echographie de datation et de calcul de la clarté nucale :</h3>
      <p>À 12 semaines d’aménorrhée (semaines sans règles), soit 2 mois et demi : elle permet de confirmer le terme de la grossesse, de déterminer le nombre d’embryons, de mesurer la clarté nucale (l’épaississement de la nuque du fœtus) et de rechercher certains signes de malformation.</p> 
      
      <h3 style="color:#6cc; margin-bottom:20px;">2. Echographie morphologique :</h3>
      <p>
        À 22 semaines d’aménorrhée, soit 4 mois et demi : elle permet de réaliser une étude morphologique complète du fœtus, d’évaluer sa croissance et de dépister certaines anomalies. Le médecin explore notamment :
        <ul>
          <li>La forme et la structure de la tête de votre bébé, son cerveau;</li>
          <li>Les éléments du visage;</li>
          <li>La taille, la forme et le fonctionnement de son cœur;</li>
          <li>L’estomac, les reins, la vessie.</li>
        </ul>
        <p>À cette période, le sexe de votre bébé est apparent. Si vous souhaitez le connaitre, demandez-le à votre médecin qui vous le montrera à l’écran s’il est visible. Sinon, il vous demandera de détourner le regard quand il s’approchera de la zone génitale.</p>
      </p> 

     <h3 style="color:#6cc; margin-bottom:20px;">3. Echographie de croissance :</h3>
      <p>À 32 semaines d’aménorrhée, soit la fin du 7e mois de grossesse : elle permet de vérifier la croissance fœtale, la présentation et la localisation placentaire.</p>

      <p>Ces échographies ne permettent pas de tout dépister. Le dépistage des anomalies et des malformations repose sur l’examen clinique et certains examens complémentaires. Votre médecin vous en expliquera le bénéfice, l’intérêt et les risques.</p>



