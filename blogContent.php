          La toxoplasmose est due à un parasite présent dans la terre, et donc sur les végétaux ou dans la viande. On peut être contaminé si l’on consomme des aliments mal lavés ou peu cuits. Une prise de sang au début de votre grossesse indique si vous avez déjà eu la toxoplasmose. Si c’est le cas, vous êtes immunisée. Sinon, vous n’êtes pas protégée et des prises de sang vous seront prescrites régulièrement pour vérifier que n’êtes pas infectée.
        </p>
